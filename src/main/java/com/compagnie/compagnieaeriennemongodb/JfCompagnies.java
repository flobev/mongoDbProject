    private void btnAjouterCompagniesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterCompagniesActionPerformed
        // TODO add your handling code here:
        if(inputNameCompagnies.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vous devez remplir les champs !", "ATTENTION", JOptionPane.INFORMATION_MESSAGE);;
            inputNameCompagnies.requestFocus();
        } else {
            String idCompagnie = inputIdCompagnies.getText();
            String designationCompagnie = inputNameCompagnies.getText();

            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con= DriverManager.getConnection("jdbc:mysql://localhost:3307/airbabouche", "root","");
                pst=con.prepareStatement("insert into compagnie (idcompagnie, nom)" +  "values(?,?)");
                pst.setString(1,idCompagnie);
                pst.setString(2,designationCompagnie);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Données enregistrées !");

                inputNameCompagnies.setText("");

                tableUpdate();

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JfCompagnies.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(JfCompagnies.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAjouterCompagniesActionPerformed

    private void btnModifierCompagniesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierCompagniesActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel dtm = (DefaultTableModel)tableCompagnie.getModel();
        int selectedIndex = tableCompagnie.getSelectedRow();
        
        try {
            
            int idComp = Integer.parseInt(dtm.getValueAt(selectedIndex, 0).toString());
            String designComp= inputNameCompagnies.getText();
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            con= DriverManager.getConnection("jdbc:mysql://localhost:3307/airbabouche", "root","");
            pst=con.prepareStatement("update compagnie set nom = ? where idcompagnie = ?");
            
            pst.setString(1, designComp);
            pst.setInt(2, idComp);
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Données mises à jour !");
            
            inputNameCompagnies.setText("");
            
            tableUpdate();
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(JfCompagnies.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(JfCompagnies.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnModifierCompagniesActionPerformed

    private void btnSupprimerCompagniesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerCompagniesActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel dtm = (DefaultTableModel)tableCompagnie.getModel();
        int selectedIndex = tableCompagnie.getSelectedRow();
            
        int idComp= Integer.parseInt(dtm.getValueAt(selectedIndex, 0).toString());
        int dialogResult = JOptionPane.showConfirmDialog(null, "Voulez-vous supprimer cette donnée ?", "Attention !", JOptionPane.YES_NO_OPTION);

        if(dialogResult==JOptionPane.YES_OPTION)
        {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con= DriverManager.getConnection("jdbc:mysql://localhost:3307/airbabouche", "root","");
                pst=con.prepareStatement("delete from compagnie where idcompagnie=?");

                pst.setInt(1, idComp);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Donnée supprimée!");

                inputNameCompagnies.setText("");

                tableUpdate();
                
            } catch (ClassNotFoundException ex) {
            Logger.getLogger(JfCompagnies.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(JfCompagnies.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSupprimerCompagniesActionPerformed

