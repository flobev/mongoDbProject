    private void btnAddPersonneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPersonneActionPerformed
        // TODO add your handling code here:
        if(inputNomPersonne.getText().equals("") || inputPrenomPersonne.getText().equals("") || inputDateNaissancePersonne.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vous devez remplir les champs !", "ATTENTION", JOptionPane.INFORMATION_MESSAGE);;
            inputNomPersonne.requestFocus();
            inputPrenomPersonne.requestFocus();
            inputDateNaissancePersonne.requestFocus();
            inputAdressePersonne.requestFocus();
            inputVillePersonne.requestFocus();
            inputZipCodePersonne.requestFocus();
            inputIdPaysPersonne.requestFocus();

        } else {
            String nomPersonne = inputNomPersonne.getText();
            String prenomPersonne = inputPrenomPersonne.getText();
            String dateNaissancePersonne = inputDateNaissancePersonne.getText();
            String adressePersonne = inputAdressePersonne.getText();
            String villePersonne = inputVillePersonne.getText();
            String zipCodePersonne = inputZipCodePersonne.getText();
            String idPaysPersonne = inputIdPaysPersonne.getText();

            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con= DriverManager.getConnection("jdbc:mysql://localhost:3307/airbabouche", "root","");
                pst=con.prepareStatement("insert into personne (nom, prenom, dateNaiss, adresse, ville, zipcode, pays_idPays) values (?,?,?,?,?,?,?)");
                pst.setString(1,nomPersonne);
                pst.setString(2,prenomPersonne);
                pst.setString(3,dateNaissancePersonne);
                pst.setString(4,adressePersonne);
                pst.setString(1,villePersonne);
                pst.setString(2,zipCodePersonne);
                pst.setString(3,idPaysPersonne);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Données enregistrées !");
                
                inputNomPersonne.setText("");
                inputPrenomPersonne.setText("");
                inputDateNaissancePersonne.setText("");
                inputAdressePersonne.setText("");
                inputVillePersonne.setText("");
                inputZipCodePersonne.setText("");
                inputIdPaysPersonne.setText("");

                tableUpdate();

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JfAeroports.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(JfAeroports.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAddPersonneActionPerformed

    private void btnUpdatePersonneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdatePersonneActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tablePersonnes.getModel();
        int selectedIndex = tablePersonnes.getSelectedRow();
        
        try {
            
            int idPersonne = Integer.parseInt(dtm.getValueAt(selectedIndex, 0).toString());
            String nomPersonne = inputNomPersonne.getText();
            String prenomPersonne = inputPrenomPersonne.getText(); 
            String dateNaissPersonne = inputDateNaissancePersonne.getText(); 
            String adressePersonne = inputAdressePersonne.getText();
            String villePersonne = inputVillePersonne.getText(); 
            String zipcodePersonne = inputZipCodePersonne.getText();
            String paysIdPays = inputIdPaysPersonne.getText();
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            con= DriverManager.getConnection("jdbc:mysql://localhost:3307/airbabouche", "root","");
            pst=con.prepareStatement("update personne set nom = ?, prenom = ?, dateNaiss = ?, adresse = ?, ville = ?, zipcode = ?, pays_idPays = ? where idpersonne = ?");
            
            pst.setString(1, nomPersonne);
            pst.setString(2, prenomPersonne);
            pst.setString(3, dateNaissPersonne);
            pst.setString(4, adressePersonne);
            pst.setString(5, villePersonne);
            pst.setString(6, zipcodePersonne);
            pst.setString(7, paysIdPays);
            pst.setInt(8, idPersonne);
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Données mises à jour !");
            
            inputNomPersonne.setText("");
            inputPrenomPersonne.setText("");
            inputDateNaissancePersonne.setText("");
            inputAdressePersonne.setText("");
            inputVillePersonne.setText("");
            inputZipCodePersonne.setText("");
            inputIdPaysPersonne.setText("");
            
            tableUpdate();
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(JfPersonnes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(JfPersonnes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdatePersonneActionPerformed

