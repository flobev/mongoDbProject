    private void btnAddEscaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEscaleActionPerformed
        // TODO add your handling code here:
        if(inputidVolEscale.getText().equals("") || inputidAeroportEscale.getText().equals("") || inputDateDepartEscale.getText().equals("") || inputDateArriveeEscale.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Vous devez remplir les champs !", "ATTENTION", JOptionPane.INFORMATION_MESSAGE);;
            inputidVolEscale.requestFocus();
        } else {
            String idVol = inputidVolEscale.getText();
            String idAeroport = inputidAeroportEscale.getText();
            String dateDepart = inputDateDepartEscale.getText();
            String dateArrivee = inputDateArriveeEscale.getText();

            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con= DriverManager.getConnection("jdbc:mysql://localhost:3307/airbabouche", "root","");
                pst=con.prepareStatement("insert into aeroport (vol_idvol, aeroport_idaeroport, date_depart, date_arrive) values (?,?,?,?)");
                pst.setString(1,idVol);
                pst.setString(2,idAeroport);
                pst.setString(3,dateDepart);
                pst.setString(4,dateArrivee);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Données enregistrées !");
                
                inputidVolEscale.setText("");
                inputidAeroportEscale.setText("");
                inputDateDepartEscale.setText("");
                inputDateArriveeEscale.setText("");

                tableUpdate();

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(JfEscales.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(JfEscales.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAddEscaleActionPerformed

    private void btnUpdateEscaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEscaleActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tableEscales.getModel();
        int selectedIndex = tableEscales.getSelectedRow();
        
        try {
            
            int idVol = Integer.parseInt(dtm.getValueAt(selectedIndex, 0).toString());
            String idAer = inputidAeroportEscale.getText();
            String dateDepart = inputDateDepartEscale.getText(); 
            String dateArrivee = inputDateArriveeEscale.getText(); 
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            con= DriverManager.getConnection("jdbc:mysql://localhost:3307/airbabouche", "root","");
            pst=con.prepareStatement("update escale set date_depart = ?, date_arrive = ? where vol_idvol = ? and aeroport_idaeroport = ?");
            
            pst.setString(1, dateDepart);
            pst.setString(2, dateArrivee);
            pst.setInt(3, idVol);
            pst.setString(4, idAer);
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Données mises à jour !");
            
            inputidAeroportEscale.setText("");
            inputDateDepartEscale.setText("");
            inputDateArriveeEscale.setText("");
            
            tableUpdate();
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(JfAeroports.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(JfAeroports.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateEscaleActionPerformed

    private void btnDeleteEscaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteEscaleActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tableEscales.getModel();
        int selectedIndex = tableEscales.getSelectedRow();
            
        int idVol = Integer.parseInt(dtm.getValueAt(selectedIndex, 0).toString());
        String idAer = inputidAeroportEscale.getText();
        int dialogResult = JOptionPane.showConfirmDialog(null, "Voulez-vous supprimer cette donnée ?", "Attention !", JOptionPane.YES_NO_OPTION);

        if(dialogResult==JOptionPane.YES_OPTION)
        {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                con= DriverManager.getConnection("jdbc:mysql://localhost:3307/airbabouche", "root","");
                pst=con.prepareStatement("delete from escale where vol_idvol=? AND aeroport_idaeroport=?");
                
                pst.setInt(1, idVol);
                pst.setString(2, idAer);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Donnée supprimée!");

                inputidAeroportEscale.setText("");
                inputDateDepartEscale.setText("");
                inputDateArriveeEscale.setText("");

                tableUpdate();
            } catch (ClassNotFoundException ex) {
            Logger.getLogger(JfAeroports.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(JfAeroports.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnDeleteEscaleActionPerformed

